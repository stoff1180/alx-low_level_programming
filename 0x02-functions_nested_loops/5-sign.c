#include "main.h"
/**
 * print_sign - prints a sign of a number
 * @n: parameters to be checked
 * Return: return 1 if greater or 0 if equal or -1 if less than zero
 */
int print_sign(int n)
{
	if (n > 0)
	{
		_putchar('+');
		return (1);
	}
	else if (n == 0)
	{
		_putchar('0');
		return (0);
	}
	else
	{
		_putchar('-');
		return (-1);
	}
}
